import static java.lang.System.*;

public class NumberVerify
{
	public static String isOdd( int num )
	{
		if (num%2 == 0)
			return "false";
		else
			return "true";
	}
	public static String isEven( int num )
	{
		if (num%2 == 0)
			return "true";
		else
			return "false";
	}	
}